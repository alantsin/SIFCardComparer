public class FinalInformation {
	
	private String finalBaseStats;
	
	private String scoreContribution;
	
	private String skillContribution;
	
	private String finalScore;

	public String getFinalBaseStats() {
		return finalBaseStats;
	}

	public void setFinalBaseStats(String finalBaseStats) {
		this.finalBaseStats = finalBaseStats;
	}

	public String getScoreContribution() {
		return scoreContribution;
	}

	public void setScoreContribution(String scoreContribution) {
		this.scoreContribution = scoreContribution;
	}

	public String getSkillContribution() {
		return skillContribution;
	}

	public void setSkillContribution(String skillContribution) {
		this.skillContribution = skillContribution;
	}

	public String getFinalScore() {
		return finalScore;
	}

	public void setFinalScore(String finalScore) {
		this.finalScore = finalScore;
	}

}
